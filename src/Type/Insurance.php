        return $this->lengthOfPolicyAgreement;
    }

    /**
     * @param string $lengthOfPolicyAgreement
     * @return Insurance
     */
    public function withLengthOfPolicyAgreement($lengthOfPolicyAgreement)
    {
        $new = clone $this;
        $new->lengthOfPolicyAgreement = $lengthOfPolicyAgreement;

        return $new;
    }

    /**
     * @return string
     */
    public function getNote()
    {
        return $this->note;
    }

    /**
     * @param string $note
     * @return Insurance
     */
    public function withNote($note)
    {
        $new = clone $this;
        $new->note = $note;

        return $new;
    }

    /**
     * @return bool
     */
