<?php

namespace BurgerDigital\Type;

class Insurance
{

    /**
     * @var string
     */
    private $beneficiary;

    /**
     * @var string
     */
    private $insurer;

    /**
     * @var string
     */
    private $lengthOfPolicyAgreement;

    /**
     * @var string
     */
    private $note;

    /**
     * @var bool
     */
    private $organizationPayingPremiums;

    /**
     * @var string
     */
    private $policyNumber;

    /**
     * @var float
     */
    private $premiumAmount;

    /**
     * @var int
     */
    private $premiumFrequency;

    /**
     * @var \DateTimeInterface
     */
    private $surrenderDate;

    /**
     * @var float
     */
    private $surrenderValue;

