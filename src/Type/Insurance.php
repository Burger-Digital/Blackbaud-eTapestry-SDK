     * @return float
     */
    public function getPremiumAmount()
    {
        return $this->premiumAmount;
    }

    /**
     * @param float $premiumAmount
     * @return Insurance
     */
    public function withPremiumAmount($premiumAmount)
    {
        $new = clone $this;
        $new->premiumAmount = $premiumAmount;

        return $new;
    }

    /**
     * @return int
     */
    public function getPremiumFrequency()
    {
        return $this->premiumFrequency;
    }

    /**
     * @param int $premiumFrequency
     * @return Insurance
     */
    public function withPremiumFrequency($premiumFrequency)
    {
        $new = clone $this;
        $new->premiumFrequency = $premiumFrequency;

        return $new;
    }

    /**
     * @return \DateTimeInterface
     */
    public function getSurrenderDate()
    {
        return $this->surrenderDate;
    }

    /**
     * @param \DateTimeInterface $surrenderDate
     * @return Insurance
     */
    public function withSurrenderDate($surrenderDate)
    {
        $new = clone $this;
        $new->surrenderDate = $surrenderDate;

        return $new;
    }

