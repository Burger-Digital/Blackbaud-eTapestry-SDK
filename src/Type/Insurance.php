        return $this->organizationPayingPremiums;
    }

    /**
     * @param bool $organizationPayingPremiums
     * @return Insurance
     */
    public function withOrganizationPayingPremiums($organizationPayingPremiums)
    {
        $new = clone $this;
        $new->organizationPayingPremiums = $organizationPayingPremiums;

        return $new;
    }

    /**
     * @return string
     */
    public function getPolicyNumber()
    {
        return $this->policyNumber;
    }

    /**
     * @param string $policyNumber
     * @return Insurance
     */
    public function withPolicyNumber($policyNumber)
    {
        $new = clone $this;
        $new->policyNumber = $policyNumber;

        return $new;
    }

